public class Relational{

public static void main(String args[]){

	int a=4;
	int b=15;
	System.out.println(a == b);
	System.out.println(a > b);
	System.out.println(a < b);
	System.out.println(a >= b);
	System.out.println(a <= b);
	System.out.println(a != b);
	System.out.println(Math.max(a, b));
	
}
}